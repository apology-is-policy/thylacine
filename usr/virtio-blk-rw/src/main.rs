// /virtio-blk-rw — multi-sector read/write driver (P4-Ic7).
//
// Closes ROADMAP §6.2's exit criterion:
//   "Userspace virtio-blk: read 1 GiB from VirtIO block device
//    successfully; write 1 GiB and read it back, verify bit-exact."
//
// Builds on virtio-blk-probe (P4-Ic5b2) by:
//   - using TWO kobj_dma allocations: a 4 KiB "ring" buffer for the
//     virtqueue rings + per-request header + status byte; a 1 MiB
//     "data" buffer that holds the per-request sector data
//     (= SECTORS_PER_REQUEST = 2048 sectors per descriptor chain).
//   - reading the VirtIO blk Capacity field (config-space offset 0)
//     and scaling the test bytes per pass to min(capacity - 1 sector,
//     MAX_PASS_BYTES). MAX_PASS_BYTES = 1 GiB so a 1 GiB disk image
//     produces a 1 GiB read pass + 1 GiB write pass + 1 GiB readback,
//     satisfying the exit criterion. Smaller disks scale down
//     proportionally.
//   - reusing the same 3-descriptor head every request (req / data /
//     status). desc[0] and desc[2] are written once; desc[1]'s
//     `flags` field flips between NEXT (for VIRTIO_BLK_T_OUT — device
//     reads data) and NEXT|WRITE (for VIRTIO_BLK_T_IN — device writes
//     data). avail.idx + used.idx track per-iteration progress.
//
// Three passes per invocation, on TWO non-overlapping regions:
//
//   READ region : sectors [1..pass_sectors+1). Holds pattern A baked
//                 in by tools/mkdisk.py. NEVER modified by this binary,
//                 so the test is idempotent (a re-run sees the same
//                 ground-truth bytes regardless of prior runs).
//
//   WRITE region: sectors [pass_sectors+1..2*pass_sectors+1). Pre-state
//                 is irrelevant (mkdisk.py also wrote pattern A there
//                 initially; a prior run may have left pattern B from
//                 its own pass B). Pass B overwrites the whole region
//                 with pattern B before pass C reads.
//
//   Pass A: READ + VERIFY pattern A over READ region. Mismatch → fail.
//   Pass B: WRITE pattern B over WRITE region. No per-sector verify.
//   Pass C: READ + VERIFY pattern B over WRITE region (write→readback).
//
// The two regions are non-overlapping (write starts at pass_sectors+1,
// one sector past the read region). The READ region is never touched
// by this binary, so re-runs are deterministic.
//
// Required disk size to meet the strict 1 GiB exit criterion: 2 GiB+.
// The binary auto-scales pass_bytes = min(half_usable_disk, 1 GiB).
// THYLACINE_DISK_SIZE=2G gives 1 GiB per pass = 1 GiB read + 1 GiB
// write + 1 GiB readback. Smaller disks scale down proportionally;
// the test still exercises the multi-sector r/w path but covers less
// territory per invocation.
//
// Pattern A is generated by tools/mkdisk.py at disk build time; the
// LCG constants are mirrored verbatim. Pattern B uses a distinct LCG
// keyed off (sector ^ PATTERN_B_SEED_XOR).
//
// IRQ wait policy: same as virtio-blk-probe. Each iteration kicks the
// device via REG_QUEUE_NOTIFY, sleeps on t_irq_wait, ACKs
// REG_INTERRUPT_STATUS, then validates used.idx + status byte (+
// data for read passes).
//
// Cap: per-pass max = 1 GiB even on larger disks. The exit criterion
// is exactly 1 GiB; arbitrary larger reads add boot-test time without
// proving anything new.

#![no_std]
#![no_main]

use core::arch::asm;
use libthyla_rs::{
    T_PROT_READ, T_PROT_WRITE, T_RIGHT_MAP, T_RIGHT_READ, T_RIGHT_SIGNAL,
    T_RIGHT_WRITE, t_dma_create, t_dma_map, t_exits, t_irq_create, t_irq_wait,
    t_mmio_create, t_mmio_map, t_puts, t_putstr,
};

// =============================================================================
// virtio-mmio bank constants — mirror of virtio-blk-probe.
// =============================================================================

const VIRTIO_MMIO_BASE_PA: u64 = 0x0a00_0000;
const VIRTIO_MMIO_SLOT_STRIDE: u64 = 0x200;
const VIRTIO_MMIO_NUM_SLOTS: u64 = 32;
const PAGE_SIZE: u64 = 0x1000;
const VIRTIO_MMIO_NUM_PAGES: u64 =
    (VIRTIO_MMIO_NUM_SLOTS * VIRTIO_MMIO_SLOT_STRIDE) / PAGE_SIZE;
const VIRTIO_MMIO_GIC_SPI_BASE: u32 = 16;
const VIRTIO_MMIO_GIC_INTID_BASE: u32 = 32 + VIRTIO_MMIO_GIC_SPI_BASE;

// User-VA layout.
const MMIO_USER_VA: u64 = 0x0050_0000;   // 4 pages → 0x500000..0x504000
const RING_USER_VA: u64 = 0x0060_0000;   // 1 page  → 0x600000..0x601000
const DATA_USER_VA: u64 = 0x0070_0000;   // 256 pages → 0x700000..0x800000

// =============================================================================
// VirtIO MMIO register offsets.
// =============================================================================

const REG_MAGIC_VALUE: u64       = 0x000;
const REG_VERSION: u64           = 0x004;
const REG_DEVICE_ID: u64         = 0x008;
const REG_DEVICE_FEATURES: u64   = 0x010;
const REG_DEVICE_FEATURES_SEL: u64 = 0x014;
const REG_DRIVER_FEATURES: u64   = 0x020;
const REG_DRIVER_FEATURES_SEL: u64 = 0x024;
const REG_QUEUE_SEL: u64         = 0x030;
const REG_QUEUE_NUM_MAX: u64     = 0x034;
const REG_QUEUE_NUM: u64         = 0x038;
const REG_QUEUE_READY: u64       = 0x044;
const REG_QUEUE_NOTIFY: u64      = 0x050;
const REG_INTERRUPT_STATUS: u64  = 0x060;
const REG_INTERRUPT_ACK: u64     = 0x064;
const REG_STATUS: u64            = 0x070;
const REG_QUEUE_DESC_LOW: u64    = 0x080;
const REG_QUEUE_DESC_HIGH: u64   = 0x084;
const REG_QUEUE_DRIVER_LOW: u64  = 0x090;
const REG_QUEUE_DRIVER_HIGH: u64 = 0x094;
const REG_QUEUE_DEVICE_LOW: u64  = 0x0a0;
const REG_QUEUE_DEVICE_HIGH: u64 = 0x0a4;
const REG_CONFIG: u64            = 0x100;     // device-specific config

const VIRTIO_MMIO_MAGIC: u32          = 0x7472_6976;
const VIRTIO_MMIO_VERSION_MODERN: u32 = 2;
const VIRTIO_DEVICE_ID_BLK: u32       = 2;

const STATUS_ACKNOWLEDGE: u32 = 1;
const STATUS_DRIVER: u32      = 2;
const STATUS_DRIVER_OK: u32   = 4;
const STATUS_FEATURES_OK: u32 = 8;
const STATUS_FAILED: u32      = 128;

const VIRTIO_F_VERSION_1_BIT_BANK1: u32 = 1 << 0;

const VIRTQ_DESC_F_NEXT: u16  = 1;
const VIRTQ_DESC_F_WRITE: u16 = 2;

const VIRTIO_BLK_T_IN: u32  = 0;
const VIRTIO_BLK_T_OUT: u32 = 1;
const VIRTIO_BLK_S_OK: u8 = 0;

// =============================================================================
// Ring DMA layout (single 4 KiB page).
// =============================================================================
//
// Holds descriptor table, avail ring, used ring, request header, and
// status byte. The data buffer is in a SEPARATE 1 MiB DMA allocation
// (the data buffer is too large to share a single kobj_dma with the
// rings + headers).
//
// VIRTIO 1.2 alignment: desc table 16-byte; avail ring 2-byte; used
// ring 4-byte. All chosen offsets respect these.

const QUEUE_SIZE: u16 = 16;
const RING_DMA_SIZE: u64 = PAGE_SIZE;

const DESC_OFF: u64   = 0x000;   // 16 × 16 B = 256 B
const AVAIL_OFF: u64  = 0x100;   // 4 + 16*2 = 36 B
const USED_OFF: u64   = 0x200;   // 4 + 16*8 = 132 B
const REQ_OFF: u64    = 0x300;   // 16 B
const STATUS_OFF: u64 = 0x310;   // 1 B

// =============================================================================
// Data DMA layout (1 MiB).
// =============================================================================

const SECTOR_SIZE: u64 = 512;
const DATA_DMA_SIZE: u64 = 1 << 20;                            // 1 MiB
const SECTORS_PER_REQUEST: u64 = DATA_DMA_SIZE / SECTOR_SIZE;   // 2048

// Maximum bytes per pass (= ROADMAP §6.2 exit criterion).
const MAX_PASS_BYTES: u64 = 1 << 30;

// =============================================================================
// Deterministic test patterns.
// =============================================================================
//
// Pattern A: per-sector LCG. Mirrored verbatim in tools/mkdisk.py.
// Each sector's 512 bytes = 64 little-endian u64s, advancing the LCG
// from initial state = sector_index.
//
// Pattern B: distinct LCG, keyed off (sector ^ PATTERN_B_SEED_XOR).
// The XOR seed-shift guarantees pattern_b(k) ≠ pattern_a(k) for all k
// (LCG-A and LCG-B have different (mul,inc); even if they accidentally
// converged, the seed XOR would un-converge them).

const PATTERN_A_MUL: u64 = 0x9E37_79B9_7F4A_7C15;
const PATTERN_A_INC: u64 = 0x1234_5678_90AB_CDEF;
const PATTERN_B_MUL: u64 = 0xBF58_476D_1CE4_E5B9;
const PATTERN_B_INC: u64 = 0x82F6_3B78_1656_67B1;
const PATTERN_B_SEED_XOR: u64 = 0xDEAD_BEEF_CAFE_BABE;

#[inline(always)]
fn advance(state: u64, mul: u64, inc: u64) -> u64 {
    state.wrapping_mul(mul).wrapping_add(inc)
}

